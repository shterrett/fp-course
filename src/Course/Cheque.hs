-- A data type representing the digits zero to nine.
data Digit =
  Zero
  | One
  | Two
  | Three
  | Four
  | Five
  | Six
  | Seven
  | Eight
  | Nine
  deriving (Eq, Ord)

instance Show Digit where
  show Zero = ""
  show One = "one"
  show Two = "two"
  show Three = "three"
  show Four = "four"
  show Five = "five"
  show Six = "six"
  show Seven = "seven"
  show Eight = "eight"
  show Nine = "nine"

-- A data type representing one, two or three digits, which may be useful for grouping.
data Digit3 =
  D1 Digit
  | D2 Digit Digit
  | D3 Digit Digit Digit
  deriving Eq

instance Show Digit3 where
    show (D1 d) = show d
    show (D2 Zero Zero) = "zero"
    show (D2 Zero d) = show d
    show (D2 One Zero) = "ten"
    show (D2 One One) = "eleven"
    show (D2 One Two) = "twelve"
    show (D2 One Three) = "thirteen"
    show (D2 One d) = show d <> "teen"
    show (D2 Two d) = "twenty " <> show d
    show (D2 Three d) = "thirty " <> show d
    show (D2 Four d) = "fourty " <> show d
    show (D2 Five d) = "fifty " <> show d
    show (D2 Eight d) = "eighty " <> show d
    show (D2 t d) = show t <> "ty " <> show d
    show (D3 h t d) = show h <> " hundred " <> show (D2 t d)

-- Possibly convert a character to a digit.
fromChar ::
  Char
  -> Optional Digit
fromChar '0' =
  Full Zero
fromChar '1' =
  Full One
fromChar '2' =
  Full Two
fromChar '3' =
  Full Three
fromChar '4' =
  Full Four
fromChar '5' =
  Full Five
fromChar '6' =
  Full Six
fromChar '7' =
  Full Seven
fromChar '8' =
  Full Eight
fromChar '9' =
  Full Nine
fromChar _ =
  Empty

-- | Take a numeric value and produce its English output.
--
-- >>> dollars "0"
-- "zero dollars and zero cents"
--
-- >>> dollars "1"
-- "one dollar and zero cents"
--
-- >>> dollars "0.1"
-- "zero dollars and ten cents"
--
-- >>> dollars "1."
-- "one dollar and zero cents"
--
-- >>> dollars "0."
-- "zero dollars and zero cents"
--
-- >>> dollars "0.0"
-- "zero dollars and zero cents"
--
-- >>> dollars ".34"
-- "zero dollars and thirty-four cents"
--
-- >>> dollars "0.3456789"
-- "zero dollars and thirty-four cents"
--
-- >>> dollars "1.0"
-- "one dollar and zero cents"
--
-- >>> dollars "1.01"
-- "one dollar and one cent"
--
-- >>> dollars "a1a"
-- "one dollar and zero cents"
--
-- >>> dollars "a1a.a0.7b"
-- "one dollar and seven cents"
--
-- >>> dollars "100"
-- "one hundred dollars and zero cents"
--
-- >>> dollars "100.0"
-- "one hundred dollars and zero cents"
--
-- >>> dollars "100.00"
-- "one hundred dollars and zero cents"
--
-- >>> dollars "100.00000"
-- "one hundred dollars and zero cents"
--
-- >>> dollars "1000456.13"
-- "one million four hundred and fifty-six dollars and thirteen cents"
--
-- >>> dollars "1001456.13"
-- "one million one thousand four hundred and fifty-six dollars and thirteen cents"
--
-- >>> dollars "16000000456.13"
-- "sixteen billion four hundred and fifty-six dollars and thirteen cents"
--
-- >>> dollars "100.45"
-- "one hundred dollars and forty-five cents"
--
-- >>> dollars "100.07"
-- "one hundred dollars and seven cents"
--
-- >>> dollars "9abc9def9ghi.jkl9mno"
-- "nine hundred and ninety-nine dollars and ninety cents"
--
-- >>> dollars "12345.67"
-- "twelve thousand three hundred and forty-five dollars and sixty-seven cents"
--
dollars ::
  Chars
  -> Chars
dollars n =
    let
      (ds, cs) = bimap (groupDigits . onlyDigits)
                       (exactlyTwo . onlyDigits)
                       $ splitDecimal n
    in
     listh
     $ show (first show <$> (zipDollarGroups ds))
     <> " dollars and "
     <> show cs
     <> " cents"
  where zipDollarGroups :: List Digit3 -> List (Digit3, Chars)
        zipDollarGroups ds =
          let
            groupings = take (length illion - length ds) illion
          in
            reverse $ zip (reverse ds) groupings

splitDecimal :: Chars -> (Chars, Chars)
splitDecimal cs =
    let
      whole = takeWhile (/= '.') cs
      fractional = drop (length whole + 1) cs
    in
      (whole, fractional)

onlyDigits :: Chars -> List Digit
onlyDigits = listOptional fromChar

groupDigits :: List Digit -> List Digit3
groupDigits = reverse . groupDigits' . reverse
  where groupDigits' (x1 :. x2 :. x3 :. xs) = (D3 x3 x2 x1) :. groupDigits' xs
        groupDigits' (x1 :. x2 :. Nil) = (D2 x2 x1) :. Nil
        groupDigits' (x1 :. Nil) = (D1 x1) :. Nil
        groupDigits' Nil = Nil

exactlyTwo :: List Digit -> Digit3
exactlyTwo (d1 :. d2 :. _) = D2 d1 d2
exactlyTwo (d1 :. _) = D2 d1 Zero
exactlyTwo _ = D2 Zero Zero
